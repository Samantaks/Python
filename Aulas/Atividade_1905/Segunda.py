PRIMEIRA_NOTA = float(input("Qual foi a primeira média bimestral que teves?"))
SEGUNDA_NOTA = float(input("Qual foi a segunda média bimestral que teves?"))
TERCEIRA_NOTA = float(input("Qual foi a terceira média bimestral que teves?"))
