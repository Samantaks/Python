
PRIMEIRO_NUMERO =  int(input("Poderias me falar um numero inteiro?"))
SEGUNDO_NUMERO = int(input("Poderias me falar outro número inteiro?"))
TERCEIRO_NUMERO = float(input("Poderias me falar agora um número do conjunto dos números reais?"))

PRODUTO = ((PRIMEIRO_NUMERO**2) * (SEGUNDO_NUMERO/2))
SOMA = ((3*PRIMEIRO_NUMERO) + TERCEIRO_NUMERO)
CUBO = TERCEIRO_NUMERO**3

print ("-----------------------------------------------")
print ("O produto do dobro do primeiro com metade do segundo será: ", PRODUTO)
print ("A soma do triplo do primeiro com o terceiro será: ", SOMA)
print ("O terceiro número elevado ao cubo será: ", CUBO)